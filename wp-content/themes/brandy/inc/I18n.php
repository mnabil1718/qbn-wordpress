	 * Load translation file from text domain
	 */
	public static function load_plugin_text_domain() {
		if ( function_exists( 'determine_locale' ) ) {
			$locale = determine_locale();
		} else {
			$locale = is_admin() ? get_user_locale() : get_locale();
		}
		unload_textdomain( 'brandy' );
		load_textdomain( 'brandy', BRANDY_TEMPLATE_DIR . '/languages/brandy-' . $locale . '.mo' );
		load_plugin_textdomain( 'brandy', false, BRANDY_TEMPLATE_URL . 'languages/' );
	}

